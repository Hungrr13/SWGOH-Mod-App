// Build a mapping of chars.js display name → swgoh.gg base_id by matching
// against (1) Hungrr's roster (authoritative but only covers owned units)
// and (2) Tosche Station's DEFID dictionary scraped from their index.html.
//
// Writes:
//   tools/roster-mapping/chars-baseid-map.json    — name → baseId
//   tools/roster-mapping/chars-baseid-unmatched.json — names needing manual review
//
// Run: node tools/map-chars-to-base-ids.js

