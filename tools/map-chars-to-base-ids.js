  `// Regenerate after any chars.js name change.\n\n` +
  `export const CHAR_BASE_IDS = ${JSON.stringify(outMap, null, 2)};\n\n` +
  `export const BASE_ID_TO_CHAR_NAME = Object.fromEntries(\n` +
  `  Object.entries(CHAR_BASE_IDS).map(([name, id]) => [id, name])\n` +
  `);\n`;
const appOut = path.resolve(__dirname, '../src/data/charBaseIds.js');
fs.writeFileSync(appOut, appModule);

